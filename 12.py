import math

# Ввід значень радіусів
R1 = float(input("Введіть радіус R1: "))
R2 = float(input("Введіть радіус R2: "))

# Обчислення площ кіл
S1 = math.pi * R1 ** 2
S2 = math.pi * R2 ** 2

# Обчислення площі кільця
S3 = S1 - S2

# Виведення результатів
print("Площа першого кола: ", S1)
print("Площа другого кола: ", S2)
print("Площа кільця: ", S3)
