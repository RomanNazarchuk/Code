a = float(input("Введіть довжину ребра куба: "))

# Обчислення об'єму куба за формулою V=a^3
V = a ** 3

# Обчислення площі поверхні куба за формулою S=6*a^2
S = 6 * a ** 2

# Виведення результату на екран
print("Об'єм куба з ребром довжиною", a, "дорівнює", V)
print("Площа поверхні куба з ребром довжиною", a, "дорівнює", S)
