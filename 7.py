# Запит вводу радіуса кола від користувача
R = float(input("Введіть радіус круга: "))

# Визначення значення π
pi = 3.14

# Обчислення довжини кола за формулою L=2*π*R
L = 2 * pi * R

# Обчислення площі круга за формулою S=π*R^2
S = pi * (R ** 2)

# Виведення результату на екран
print("Довжина кола з радіусом", R, "дорівнює", L)
print("Площа круга з радіусом", R, "дорівнює", S)
